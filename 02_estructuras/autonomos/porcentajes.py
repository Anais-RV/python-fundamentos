# EJERCICIO AUTÓNOMO 4: PORCENTAJES
# Enunciado: calcula el 15% de una cantidad.

num = input("Introduce un número: ")

num = int(num)

porcentaje = 0.15

resultado = num * porcentaje

print(f"15% de {num} --> {resultado}")

